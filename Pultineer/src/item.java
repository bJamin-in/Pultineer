public class item {
    //variables
    private String name;
    private int price, buff;
    
    //Getters
    public String getName() {
        return name;
    }
    public int getPrice() {
        return price;
    }
    public int getBuff() {
        return buff;
    }

    //Setters
    public void setName(String name) {
        this.name = name;
    }
    public void setPrice(int price) {
        this.price = price;
    }
    public void setBuff(int buff) {
        this.buff = buff;
    }

}
